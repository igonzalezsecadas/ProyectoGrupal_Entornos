package concesionario;

public class empleados {
	
	
		    private String nombre;
		    private String dni;
		    private int tamaño;
		    private String direccion;
		    
		    public empleados() {
		        nombre = "";
		        dni = "";
		        tamaño = 0;
		        direccion="";
		    }

		    // Constructor con parámetros
		    public empleados(String nombre, String dni, int tamaño) {
		        this.nombre = nombre;
		        this.dni = dni;
		        this.tamaño = tamaño;
		        this.direccion=direccion;
		    }


}
